package com.example.ui.model;

public class RenderIndiceAllocation {

	String label;
	double y;
	
	public RenderIndiceAllocation(String label, double y) {
		super();
		this.label = label;
		this.y = y;
	}
	
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	
}
